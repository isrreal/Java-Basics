public class IfElse {
    public static void main(String args[]) {
        int time = 22;
        if (time < 10) 
            System.out.println("Bom dia.");             
        else if (time > 20) 
            System.out.println("Boa noite.");    
        else 
            System.out.println("Boa tarde.");       
    }
}
